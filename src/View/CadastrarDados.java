        if (eyeInput.getText().isEmpty() || visionInput.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Informe todos os campos");
        } else {
            int eye_input = Integer.parseInt(eyeInput.getText());
            int vision_input = Integer.parseInt(visionInput.getText());
            DataClientVisit dataVisitClient = new DataClientVisit(eye_input, vision_input);
            Visita visita = new Visita(client.getId());

            boolean response = VisitaDAO.InsertNewVisit(visita, dataVisitClient);

            ConsultarVisita consultarVisita = new ConsultarVisita(visita.getClientId());
            consultarVisita.setVisible(true);
            CadastrarDados.this.setVisible(false);
          
        }
